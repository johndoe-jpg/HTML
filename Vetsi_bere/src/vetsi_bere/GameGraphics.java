    private void drawPlayer(int player, int x, int y, Graphics g) {
        if(player >= PlayerManager.getPlayers().size()) {
            return;
        }
        // Max players = 8
        // 2 rows, 4 cols
        int w = getWidth() / 4;
        int h = getHeight() / 4;
        if(newGame > 1) {
            if(PlayerManager.getPlayers().get(player).peek() != null) {
                PlayerManager.getPlayers().get(player).peek().drawCard(g, new Point(x * w, y * h), h);
            }
        }
        if(PlayerManager.getPlayers().get(player).cardsLeft() != 0) {
            c.drawCard(g, new Point(x * w + w / 2, y * h), h);
        }
    }

