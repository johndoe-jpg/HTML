    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        g.setColor(Color.white);
        g.fillRect(0, 0, 1000, 1000);
        drawPlayer(5, 0, 0, g);
        drawPlayer(1, 1, 0, g);
        drawPlayer(3, 2, 0, g);
        drawPlayer(7, 3, 0, g);
        drawPlayer(6, 0, 3, g);
        drawPlayer(2, 1, 3, g);
        drawPlayer(0, 2, 3, g);
        drawPlayer(4, 3, 3, g);
        newGame++;
    }

