    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_W:
            case KeyEvent.VK_UP:
                firePropertyChange("changeDir", null, Direction.NORTH);
                break;
            case KeyEvent.VK_D:
            case KeyEvent.VK_RIGHT:
                firePropertyChange("changeDir", null, Direction.WEST);
                break;
            case KeyEvent.VK_S:
            case KeyEvent.VK_DOWN:
                firePropertyChange("changeDir", null, Direction.SOUTH);
                break;
            case KeyEvent.VK_A:
            case KeyEvent.VK_LEFT:
                firePropertyChange("changeDir", null, Direction.EAST);
                break;
            default:
        }
    }//GEN-LAST:event_formKeyPressed

    @Override
    public void propertyChange(PropertyChangeEvent pce) {
        if(pce.getPropertyName().equals("newGame")) {
            jButton1.setEnabled(true);
            setLocation((Point) pce.getNewValue());
            setVisible(true);
            gm.newGame();
        }
        if(pce.getPropertyName().equals("backToMenu")) {
            backToMenu();
        }
        if(pce.getPropertyName().equals("gameOver")) {
            jButton1.setEnabled(false);
            gow.setLocation(getLocation());
        }
    }

